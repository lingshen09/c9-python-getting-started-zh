# You can use the + operator to concatenate strings  你可以使用+号来拼接字符串
first_name = 'Susan'
last_name = 'Ibach'
print(first_name + last_name)

# If you want a space between the strings you must include the space 如果你希望字符串中包含空格，你也必须在字符串中加上空格
# within the string
print('Hello ' + first_name + ' ' + last_name)
