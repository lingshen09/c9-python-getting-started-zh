# Each print statements starts on a new line每个打印占用新的一行
print('Hello world')

# If you pass nothing to the print statement you get a blank line  如果什么都不传，那就会打印一个空行
print()
print('Did you see that blank line?')

# '\n' is a special character sequence that means print new line 使用'\n'也可以指定输出换行
# you can use it to break the output over multiple lines  使用这个符合可以实现多行输出，例如：
print('Blank line \nin the middle of string')



