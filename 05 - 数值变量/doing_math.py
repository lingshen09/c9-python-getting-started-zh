# Because the variables are assigned numeric values when created  当变量声明时就是数值
# Python knows they are numeric variables  Python知道他们应该按数值处理
first_num = 6
second_num = 2

# You can peform a variety of math operations on numeric values  你可以使用许多数学方法操作他们
print('addition')
print(first_num + second_num)
print('subtraction')
print(first_num - second_num)
print('multiplication')
print(first_num * second_num)
print('division')
print(first_num / second_num)
print ('exponent')
print(first_num ** second_num)
